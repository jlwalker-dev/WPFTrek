/*
 * Public class for creating a movement track to calculate
 * actions and animation for the game
 */
namespace WPFTrek
{
    class Track
    {
        private int row;  // row
        private int col;  // col

        public int Row { get => row; set => row = value; }
        public int Col { get => col; set => col = value; }
    }
}
